#include <stddef.h>

//=============================================================================
// append S to D
//=============================================================================
char* strcat (char *d, const char *s)
{
	char *tmp = d;

	while ('\0' != *d)
		d++;

	while ('\0' != *s)
		*d++ = *s++;

	*d = '\0';

	return tmp;
}
